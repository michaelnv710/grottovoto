      <h2>Conception</h2>
      <p>The real roots of my exploration probably seeded way back in the formative years... but this pretty much all started a year or two ago (2014) when a friend mentioned "The Labyrinth" of utility tunnels underneath downtown St. Paul. Naturally, he brought up <a href="http://www.actionsquad.org">Action Squad</a> and the Urban Exploration they had done all over town several years ago. "I think they have a website or something" was the underestimation that lead me to find their multitude of stories about caves and tunnels and buildings. I could relate to Max Action's <a href="http://www.actionsquad.org/sitehistory.htm">depiction</a> of the first time he found out about local exploring websites, only that he had already been exploring five years while for me this discovery is what lead me to explore.</p>
